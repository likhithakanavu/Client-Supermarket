  const [successMsg, setSuccessMsg] = useState("");
  const [errors, setErrors] = useState({});

  const validate = () => {
    let tempErrors = {};

    // Full Name validation
    tempErrors.clientName = clientName ? "" : "Full Name is required.";

    // Email validation
    const emailRegex = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
    tempErrors.email = email
      ? emailRegex.test(email) && email.endsWith('@gmail.com')
        ? ""
        : "Email must be a valid Gmail address."
      : "Email is required.";

    // Phone Number validation
    const phoneRegex = /^[0-9]{10}$/;
    tempErrors.phone = phone
      ? phoneRegex.test(phone)
        ? ""
        : "Phone number must be exactly 10 digits."
      : "Phone number is required.";

    // Password validation
    const passwordRegex = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]{8,}$/;
    tempErrors.password = password
      ? passwordRegex.test(password)
        ? ""
        : "Password must be at least 8 characters long and include one uppercase letter, one lowercase letter, one number, and one special character."
      : "Password is required.";

    setErrors(tempErrors);
    return Object.values(tempErrors).every(x => x === "");
  };

  const handleSubmit = () => {
    if (validate()) {
      axios
        .post("http://localhost:7001/user/reg", {
          name: clientName,
          email: email,
          phone: phone,
          password: password,
        })
        .then((res) => {
          alert(res.data.message);
          if (res.data.success) {
            navigate('/signin');
          }
        })
        .catch((err) => {
          console.log(err);
          alert(err.message);
        });
    } else {
      alert("Please fill in all fields correctly.");
    }
  };

